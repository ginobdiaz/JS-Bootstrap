let temp = 31;
//let isFreezing = false;

// === is the equality operator
let isFreezing = temp === 31; //gives boolean based off comparison
console.log(isFreezing);

// !== is the not equal operator
isFreezing = temp !== 31;
console.log(isFreezing);

// < is a less than operator
isFreezing = temp < 32;
console.log(isFreezing)

// > is a greater than operator
isFreezing = temp > 32
console.log(isFreezing);

